import { v2 as cloudinary } from 'cloudinary';
import fs from 'fs';
import dotenv from 'dotenv';

dotenv.config(); 

cloudinary.config({ 
  cloud_name: process.env.CLOUDINARY_CLOUD_NAME,
  api_key: process.env.CLOUDINARY_API_KEY,
  api_secret: process.env.CLOUDINARY_API_SECRET,
});

const uploadToCloudinary = async (localFilePath) => {
  if (!localFilePath) {
    console.error("‚ùå No local file path provided");
    return null;
  }

  console.log("üì§ Uploading file:", localFilePath);

  try {
    const response = await cloudinary.uploader.upload(localFilePath, {
      resource_type: 'auto',
    });

    console.log('‚úÖ File uploaded to Cloudinary:', response.url);
    return response;
  } catch (error) {
    console.error('‚ùå Cloudinary Upload Error:', error?.response?.body || error.message || error);
    return null;
  } finally {
    try {
      fs.unlinkSync(localFilePath);
      console.log('üßπ Local file deleted:', localFilePath);
    } catch (fsErr) {
      console.error('‚ö†Ô∏è Error deleting local file:', fsErr.message);
    }
  }
};

export { uploadToCloudinary };
